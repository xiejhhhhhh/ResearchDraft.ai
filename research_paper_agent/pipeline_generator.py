def zscore(x: np.ndarray) -> np.ndarray:
    """Standardize a feature block while preserving missing values."""
    x = np.asarray(x, dtype=float)
    return (x - np.nanmean(x, axis=0, keepdims=True)) / (np.nanstd(x, axis=0, keepdims=True) + 1e-8)


def temporal_window_mean(series: np.ndarray, window: int = 3) -> np.ndarray:
    """Aggregate observations into rolling temporal windows."""
    series = np.asarray(series, dtype=float)
    output = []
    for idx in range(series.shape[0]):
        start = max(0, idx - window + 1)
        output.append(np.nanmean(series[start:idx + 1], axis=0))
    return np.stack(output, axis=0)


def quality_weighted_fusion(blocks: list[np.ndarray], quality: np.ndarray | None = None) -> np.ndarray:
    """Fuse multimodal feature blocks using source-quality weights."""
    normalized = [zscore(block) for block in blocks if block is not None]
    if quality is None:
        quality = np.ones(len(normalized), dtype=float)
    quality = np.asarray(quality, dtype=float)
    quality = quality / (quality.sum() + 1e-8)
    return np.concatenate([weight * block for weight, block in zip(quality, normalized)], axis=-1)


def build_multimodal_features(
    photometry_or_remote_sensing: np.ndarray | None = None,
    spectra_or_environment: np.ndarray | None = None,
    catalog_or_static_features: np.ndarray | None = None,
    image_features: np.ndarray | None = None,
    auxiliary_features: np.ndarray | None = None,
) -> np.ndarray:
    """Construct model-ready features from heterogeneous scientific data sources."""
    blocks = []
    if photometry_or_remote_sensing is not None:
        blocks.append(temporal_window_mean(photometry_or_remote_sensing))
    if spectra_or_environment is not None:
        blocks.append(temporal_window_mean(spectra_or_environment))
    if catalog_or_static_features is not None:
        blocks.append(catalog_or_static_features)
    if image_features is not None:
        blocks.append(image_features)
    if auxiliary_features is not None:
        blocks.append(auxiliary_features)
    if not blocks:
        raise ValueError("At least one data source must be provided.")
    return quality_weighted_fusion(blocks)


