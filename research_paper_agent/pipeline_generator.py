def softmax(logits: np.ndarray) -> np.ndarray:
    logits = np.asarray(logits, dtype=float)
    logits = logits - np.max(logits, axis=-1, keepdims=True)
    exp = np.exp(logits)
    return exp / (np.sum(exp, axis=-1, keepdims=True) + 1e-8)


def linear_agent(features: np.ndarray, weights: np.ndarray) -> np.ndarray:
    """A lightweight stand-in for a source-specific predictive agent."""
    return softmax(np.asarray(features) @ np.asarray(weights))


def reliability_from_validation(f1_scores: np.ndarray) -> np.ndarray:
    """Convert validation performance into agent-fusion weights."""
    scores = np.maximum(np.asarray(f1_scores, dtype=float), 1e-6)
    return scores / scores.sum()


def fuse_agent_outputs(agent_outputs: list[np.ndarray], reliability: np.ndarray) -> np.ndarray:
    """Fuse predictions from multiple data-source-specific agents."""
    reliability = reliability_from_validation(reliability)
    stacked = np.stack(agent_outputs, axis=0)
    return np.tensordot(reliability, stacked, axes=(0, 0))


def event_risk(probabilities: np.ndarray, event_class: int = 1) -> np.ndarray:
    """Return the event probability used for forecasting or early-warning outputs."""
    return np.asarray(probabilities)[..., event_class]


def temporal_risk_smoothing(risk: np.ndarray, alpha: float = 0.65) -> np.ndarray:
    """Smooth disease risk through the growing season."""
    risk = np.asarray(risk, dtype=float)
    output = np.zeros_like(risk)
    output[0] = risk[0]
    for idx in range(1, len(risk)):
        output[idx] = alpha * risk[idx] + (1 - alpha) * output[idx - 1]
    return output


